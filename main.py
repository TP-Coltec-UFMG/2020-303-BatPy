#todas as informações do pacman estarão nesta classe
class Morcego:
    #construtor
    def __init__(self, tamanho):
        #posição que será desenhado
        self.coluna = 0
        self.linha = 1
        self.coluna_intencao = self.coluna
        self.linha_intencao = self.linha
        self.centro_x = 400
        self.centro_y = 300
        self.tamanho = tamanho
        self.raio = int(self.tamanho / 2)
        self.mode = 1
        #velocidade
        self.vel_x = 0
        self.vel_y = 0


    def calcular_regras(self):
        #self.coluna = self.coluna + self.vel_x
        #self.linha = self.linha + self.vel_y
        self.coluna_intencao = self.coluna+self.vel_x
        self.linha_intencao = self.linha+self.vel_y
        self.centro_x = int(self.coluna * self.tamanho + self.raio)
        self.centro_y = int(self.linha * self.tamanho + self.raio)

    def aceitar_movimento(self):
        self.coluna=self.coluna_intencao
        self.linha = self.linha_intencao

    def pintar(self, tela):
        # corpo
        pygame.draw.circle(tela, MARROM, (self.centro_x, self.centro_y), self.raio, 0)

    def processar_eventos(self, eventos):
        for e in eventos:
            if e.type == pygame.KEYDOWN:
                if e.key == pygame.K_RIGHT:
                    self.vel_x = 1
                    self.mode = 1
                if e.key == pygame.K_LEFT:
                    self.vel_x = -1
                    self.mode = 2
                if e.key == pygame.K_UP:
                    self.vel_y = -1
                    self.mode = 3
                if e.key == pygame.K_DOWN:
                    self.vel_y = 1
                    self.mode = 4
            elif e.type == pygame.KEYUP:
                if e.key == pygame.K_RIGHT:
                    self.vel_x = 0
                if e.key == pygame.K_LEFT:
                    self.vel_x = 0
                if e.key == pygame.K_UP:
                    self.vel_y = 0
                if e.key == pygame.K_DOWN:
                    self.vel_y = 0

